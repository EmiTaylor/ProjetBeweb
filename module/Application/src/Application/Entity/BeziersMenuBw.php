<?php

namespace Application\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * BeziersMenuBw
 *
 * @ORM\Table(name="beziers_menu_bw")
 * @ORM\Entity
 */
class BeziersMenuBw
{
