<?php

namespace Application\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * BeziersContactBw
 *
 * @ORM\Table(name="beziers_contact_bw")
 * @ORM\Entity
 */
class BeziersContactBw
{
